using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Project2
{
    internal class Calculator
    {
        public double sumBothNumbers(double num1, double num2)
        {
            return num1 + num2;
        }

        public double subBothNumbers(double num1, double num2)
        {
            return num1 - num2;
        }

        public double multiplyBothNumbers(double num1, double num2)
        {
            return num1 * num2;
        }

        public double divideBothNumbers(double num1, double num2)
        {
            return num1 / num2;
        }
    }
}
